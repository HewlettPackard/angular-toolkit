import {Component, Input} from '@angular/core';

//=============================================================================

@Component({
	selector    :     'hpe-rounded-text',
	templateUrl :   './rounded-text.html',
	styleUrls   : [ './rounded-text.scss' ]
})

//=============================================================================

export class RoundedText {

	//-------------------------------------------------------------------------
	//---
	//--- Variables
	//---
	//-------------------------------------------------------------------------

	@Input() color: string;

	//-------------------------------------------------------------------------

	displayedText : any;

	//-------------------------------------------------------------------------

	private _text: any;

	//-------------------------------------------------------------------------
	//---
	//--- Constructor
	//---
	//-------------------------------------------------------------------------

	constructor() {
		this.text  = null;
		this.color = "green";
	}

	//-------------------------------------------------------------------------
	//---
	//--- API methods
	//---
	//-------------------------------------------------------------------------

	get text(): any {
		return this._text;
	}

	//-------------------------------------------------------------------------

	@Input()
	set text(value: any) {

		this._text = value;

		if (value == null) {
			this.displayedText = "...";
		}
		else {
			this.displayedText = value;
		}
	}

	//-------------------------------------------------------------------------

	getClassColor() : string[] {

		let styles: string[] = ["roundedStyle"];

		if (this.color != null) {
			styles.push(this.color);
		}

		return styles;
	}
}

